# tipo de dado bool (boolean)
# ao questionar algo em um programa so existem duas respostas
# sim (True) ou não (False)
# existem vários operadores para "questionar"
# dentre eles o ==
# que é um operador lógico que qestiona se um valor é igual a outro
print(10 == 10) # Sim -> True (Verdadeiro)
print(10 == 10) # Não => False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))